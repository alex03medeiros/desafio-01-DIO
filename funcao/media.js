/*Função que calcula média de três números.*/

function media(nota1,nota2,nota3){
    console.log ("A média é:",((nota1+nota2+nota3)/3))
    console.log("-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-")
}

media(6,7,5)

function med(n1,n2,n3,n4){
    return ((n1+n2+n3+n4)/4)
}

let resultado = med(6,5,7,10) 
console.log("A média é:",resultado)
console.log("-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-")
